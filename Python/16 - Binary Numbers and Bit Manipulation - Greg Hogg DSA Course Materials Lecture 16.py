# Decimal to Binary
print(bin(5)[2:])

# Convert Binary to Decimal
binary_5 = '101'
print(int(binary_5, 2))

# Bitwise AND
print(5 & 7)

# Bitwise OR
print(5 | 7)

# Bitwise XOR
print(5 ^ 7)

# Bitwise NOT
print(~5)

# Left Shift
print(5 << 2)

# Arithmetic (Signed) Right Shift
print(5 >> 1)

# Hexadecimal (Base 16)
print(hex(890))